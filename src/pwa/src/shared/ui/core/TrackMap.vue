<script setup lang="ts">
import { ref, watch, onMounted } from 'vue'

const props = withDefaults(defineProps<{
  carProgress?: number | null // 0 to 100
  strokeClass?: string
  activeTurnId?: number | null
  dangerTurns?: number[]
  layout?: 'nascar' | 'gp'
}>(), {
  layout: 'gp'
})

const trackPath = ref<SVGPathElement | null>(null)
const carCx = ref(0)
const carCy = ref(0)

import { gpMainPath, gpAlternates, nascarMainPath, nascarAlternates } from './track_paths'
import { computed } from 'vue'

const activeMainPath = computed(() => props.layout === 'gp' ? gpMainPath : nascarMainPath)
const activeAlternates = computed(() => props.layout === 'gp' ? gpAlternates : nascarAlternates)

const updateCarPos = () => {
  if (trackPath.value && props.carProgress !== undefined && props.carProgress !== null) {
    try {
      const totalLength = trackPath.value.getTotalLength()
      const point = trackPath.value.getPointAtLength((props.carProgress / 100) * totalLength)
      carCx.value = point.x
      carCy.value = point.y
    } catch (e) {
      // ignore
    }
  }
}

const getPointAtProgress = (progress: number) => {
  if (!trackPath.value) return { x: 0, y: 0 }
  const len = trackPath.value.getTotalLength()
  return trackPath.value.getPointAtLength((progress / 100) * len)
}

import { trackTurns } from './track_turns'

defineExpose({ getPointAtProgress, trackTurns })

watch(() => props.carProgress, updateCarPos)
onMounted(updateCarPos)
